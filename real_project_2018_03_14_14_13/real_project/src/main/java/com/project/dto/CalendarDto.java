package com.project.dto;

import java.sql.Date;

//달력 테이블
public class CalendarDto {
	
	private int cal_idx;//캘린더 인덱스
	private String m_nick;//닉네임
	private String cal_type;//분류
	private Date cal_date;//날짜
	
	public int getCal_idx() {
		return cal_idx;
	}
	public void setCal_idx(int cal_idx) {
		this.cal_idx = cal_idx;
	}
	public String getM_nick() {
		return m_nick;
	}
	public void setM_nick(String m_nick) {
		this.m_nick = m_nick;
	}
	public String getCal_type() {
		return cal_type;
	}
	public void setCal_type(String cal_type) {
		this.cal_type = cal_type;
	}
	public Date getCal_date() {
		return cal_date;
	}
	public void setCal_date(Date cal_date) {
		this.cal_date = cal_date;
	}
	
}
